const calcMetrics = (history) => {
  if (history.length < 14) return null;
  const peak = Math.max(...history.map(d => d.streams));
  const eff = history.slice(0, Math.min(60, history.length));
  const n = eff.length, mX = (n-1)/2;
  const mY = eff.reduce((s,d)=>s+Math.log(Math.max(d.streams,1)),0)/n;
  const numK = eff.reduce((s,d,i)=>s+(i-mX)*(Math.log(Math.max(d.streams,1))-mY),0);
  const denK = eff.reduce((s,_,i)=>s+(i-mX)**2,0);
  const structK = Math.max(0, -(denK>0?numK/denK:0));
  const rec = history.slice(-Math.min(28,history.length));
  const rn = rec.length, rMx=(rn-1)/2;
  const rMy = rec.reduce((s,d)=>s+Math.log(Math.max(d.streams,1)),0)/rn;
  const rNum = rec.reduce((s,d,i)=>s+(i-rMx)*(Math.log(Math.max(d.streams,1))-rMy),0);
  const rDen = rec.reduce((s,_,i)=>s+(i-rMx)**2,0);
  const recentK = Math.max(0, -(rDen>0?rNum/rDen:0));
  const halfLife = structK>0 ? +(Math.log(2)/structK/7).toFixed(1) : 999;
  const recAvg = rec.reduce((s,d)=>s+d.streams,0)/rec.length;
  const organicFloor = +((recAvg/peak)*100).toFixed(1);
  const prev4w = history.slice(-Math.min(56,history.length), -Math.min(28,history.length));
  const prevAvg = prev4w.length ? prev4w.reduce((s,d)=>s+d.streams,0)/prev4w.length : recAvg;
  const mom4w = +(((recAvg-prevAvg)/prevAvg)*100).toFixed(1);
  return {
    structK:+structK.toFixed(4), recentK:+recentK.toFixed(4),
    halfLife, organicFloor, mom4w, peak,
    current:history.at(-1)?.streams??0, avgStreams:Math.round(recAvg),
  };
};

// ════════════════════════════════════════════════════════════════
//  BUILD CATALOG
// ════════════════════════════════════════════════════════════════
const buildCatalog = (liveData = {}) => TRACKS_CFG.map(cfg => {
  const liveRaw = liveData[cfg.id] ?? null;
  const raw = liveRaw ?? RAW_STREAM_DATA[cfg.id];
  if (!raw) return null;
  const histRaw = buildHistoryFromReal(cfg.id, liveRaw);
  const history = computeBaseline(histRaw, cfg.dmStart);
  const metrics = calcMetrics(history);
  let dm = { liftPct:0, incremental:0, gross:0, commission:0, net:0, observed:0, baseline:0 };
  if (cfg.dmStart != null) {
    const camp = history.slice(cfg.dmStart, cfg.dmEnd ?? history.length);
    const obs = camp.reduce((s,d)=>s+d.streams,0);
    const bl  = camp.reduce((s,d)=>s+(d.baseline??d.streams),0);
    const inc = obs - bl;
    const gross = inc * cfg.royalty;
    const commPct = (cfg.sources.editorial + cfg.sources.algorithmic)/100;
    const comm = gross * commPct * CFG.DM_CUT;
    dm = {
      liftPct: bl>0 ? +((inc/bl)*100).toFixed(1) : 0,
      incremental: Math.round(inc), gross:+gross.toFixed(2),
      commission:+comm.toFixed(2), net:+(gross-comm).toFixed(2),
      observed:obs, baseline:bl,
    };
  }
  return { ...cfg, name:raw.name, artist:raw.artist, history, metrics, dm };
}).filter(Boolean);

