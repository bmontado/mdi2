            <Tooltip contentStyle={{background:"#0f172a",border:"1px solid #1e293b",borderRadius:"10px",fontSize:"11px"}}
              formatter={(val,name)=>name==="Revenue Neto"?[fmt.usd(val),name]:[val.toLocaleString("es-AR"),name]} />
